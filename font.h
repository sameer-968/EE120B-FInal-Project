void draw_char(uint8_t x, uint8_t y, char c, uint16_t color) {
    if (c == ' ') {
        Send_Command(0x2A);
        Send_Data(0x00); Send_Data(x + 4);
        Send_Command(0x2B); 
        Send_Data(0x00); Send_Data(y);
        Send_Data(0x00); Send_Data(y + 6);
        Send_Command(0x2C); 
        for (uint8_t i = 0; i < 5*7; i++) {
            Send_Data(0x00); Send_Data(0x00); 
        }
        return;
    }


    uint8_t index = c - 'A';

    Send_Command(0x2A); 
    Send_Data(0x00); Send_Data(x);
    Send_Data(0x00); Send_Data(x + 4);

    Send_Command(0x2B); 
    Send_Data(0x00); Send_Data(y);
    Send_Data(0x00); Send_Data(y + 6);

    Send_Command(0x2C);

    for (uint8_t row = 0; row < 7; row++) {
        for (uint8_t col = 0; col < 5; col++) {
            uint8_t line = pgm_read_byte(&font5x7[index][col]);
            uint8_t pixel_on = (line >> row) & 0x01;

            uint16_t pixel_color = pixel_on ? color : 0x0000; 
            Send_Data(pixel_color >> 8);
            Send_Data(pixel_color & 0xFF);
        }
    }
}

void draw_text(uint8_t x, uint8_t y, const char *text, uint16_t color) {
    while (*text) {
        draw_char(x, y, *text++, color);
        x += 6; 
    }
}
